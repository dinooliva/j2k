class IfJa {
    int ifTernary(boolean b) {
        return b ? 1 : 0;
    }
    int ifThen(boolean b) {
        if (b) {
            return 1;
        }
        return 0;
    }

    int ifThenElse(boolean b) {
        if (b) {
            return 1;
        } else {
            return 0;
        }
    }
}
